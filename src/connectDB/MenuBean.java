package connectDB;

public class MenuBean {
	private int idx;
	private int shop_idx;
	private String menu_name;
	private int menu_type;
	private String menu_infor;
	private int price;
	private int price_s;
	private int price_m;
	private int price_l;
	private String menu_photo;
	private int menu_order;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}

	public int getShop_idx() {
		return shop_idx;
	}
	public void setShop_idx(int shop_idx) {
		this.shop_idx = shop_idx;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public int getMenu_type() {
		return menu_type;
	}
	public void setMenu_type(int menu_type) {
		this.menu_type = menu_type;
	}
	public String getMenu_infor() {
		return menu_infor;
	}
	public void setMenu_infor(String menu_infor) {
		this.menu_infor = menu_infor;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getPrice_s() {
		return price_s;
	}
	public void setPrice_s(int price_s) {
		this.price_s = price_s;
	}
	public int getPrice_m() {
		return price_m;
	}
	public void setPrice_m(int price_m) {
		this.price_m = price_m;
	}
	public int getPrice_l() {
		return price_l;
	}
	public void setPrice_l(int price_l) {
		this.price_l = price_l;
	}
	public String getMenu_photo() {
		return menu_photo;
	}
	public void setMenu_photo(String menu_photo) {
		this.menu_photo = menu_photo;
	}
	public int getMenu_order() {
		return menu_order;
	}
	public void setMenu_order(int menu_order) {
		this.menu_order = menu_order;
	}
}
